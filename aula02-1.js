//Verifique se um número é positivo.
const num = 8;
if(num >= 0) {
    console.log("Esse número é positivo.")
} else {
    console.log("Esse número é negativo.")
}

//Verifique se um número é par
if (num % 2 == 0) {
    console.log("Esse número é par")
} else {
    console.log("Esse número é impar.")
}

//Verifique se uma string é vazia
let string = "";
if(string === "") {
    console.log("Essa string está vazia")
} else {
    console.log(string)
}

//Verifique se um número é maior que 10.
if (num > 10) {
    console.log("Esse número é maior que 10")
} else {
    console.log(num * 2)
}

